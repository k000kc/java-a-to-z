package ru.apetrov.converter;

import org.junit.Test;

import java.util.Iterator;

import static org.hamcrest.core.Is.is;
import static org.junit.Assert.*;

/**
 * Created by Andrey on 28.02.2017.
 */
public class IteratorOfIteratorsTest {

    @Test
    public void whenNextThenIterator() {
        IteratorOfNumbers i1 = new IteratorOfNumbers(new int[]{1, 2, 3});
        IteratorOfNumbers i2 = new IteratorOfNumbers(new int[]{4, 5, 6});
        IteratorOfNumbers i3 = new IteratorOfNumbers(new int[]{7, 8, 9});

        IteratorOfIterators iterators = new IteratorOfIterators(new Iterator[]{i1, i2, i3});

        iterators.next();
        Iterator result = iterators.next();

        assertThat(result, is(i2));
    }

    @Test
    public void whenNotHasNextTheanFalse() {
        IteratorOfNumbers i1 = new IteratorOfNumbers(new int[]{1, 2, 3});

        IteratorOfIterators iterators = new IteratorOfIterators(new Iterator[]{i1});

        iterators.next();
        boolean result = iterators.hasNext();

        assertThat(result, is(false));
    }

}